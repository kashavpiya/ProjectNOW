                <h2>Eviction Diversion Program</h2>
                <p>According to NCSC's 2015 report <a href="https://ncsc.contentdm.oclc.org/digital/collection/civil/id/168/rec/3" target="_blank">The Landscape of Civil Litigation in State Courts</a> about 20% of civil cases in the United States involve landlords and tenants. While several states have adopted eviction moratoria state court systems are also moving to develop eviction diversion programs… And that is what is happening here in Rock Island County.</p>
             
                <p>Rock Island County's Eviction Diversion Program connects tenants & landlords to local not-for-profit agencies and court-trained mediators to help keep people in their homes. The 14th Judicial Court is mandating the Eviction Diversion Program be followed when an eviction is filed in Rock Island County. The court will provide trained mediators (John Deere Attorneys) and access to legal services and community resources—like Project NOW who can provide rental funds, mortgage relief and other financial assistance. The pro-bono John Deere attorneys will be trained May 3rd.</p>
                
                <p>To learn more about the program you can view this Facebook discussion with 14th Judicial Circuit Court Judge Carol Pentuic, Attorney Michelle Fitzsimmons with Prairie State Legal Services Inc., Attorney Jennifer Kincaid with the Rock Island County Bar Association, Ron Lund-Project NOW’s Community Services Director and the Executive Director of Project NOW, Dwight Ford.</p>
                
                <p>Please bring the following when meeting with Project NOW:
                <ol>
                    <li>Photo ID</li><br>
                    <li>Social Security Cards for everyone in the household</li><br>
                    <li>Income for the last 30 days (you are not required to have income to qualify)</li><br>
                    <li>Lease</li><br>
                    <li>Eviction Notice</li><br>
                    <li>Rental payment ledger from your landlord</li><br>
                    
                </ol></p>
                
